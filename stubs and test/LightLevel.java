public enum LightLevel {
	/**
	 * 	Filter the plants that live well full shaded area
	 */
	SHADE, 
	
	/**
	 *  Filter the plants that live well in partly-shaded areas
	 */
	PARTLY_SHADE, 
	
	/**
	 *  Filter the plants that live well sunny areas
	 */
	SUNNY;
}
